package JavaArithmeticOperators;
public class Addition {
    public static void main(String[] args) {
        //addition
        int p = 10;
        int q = 25;
        int r = p+q;
        System.out.println("add" +r);
        //2
        long a = 2222;
        long b = 1111;
        long c = a+b;
        System.out.println("add" +c);
        //3
        double num1 = 1.50;
        double num2 = 3.00;
        double sum = num1+num2;
        System.out.println("add" +sum);
    }
}
