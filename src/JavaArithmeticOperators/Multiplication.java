package JavaArithmeticOperators;
public class Multiplication {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 8;
        int num3 = num1 * num2;
        System.out.println("mul" +num3);
        //2
        float v = 8.0f;
        float s = 5.0f;
        float n = v * s;
        System.out.println("mul" +n);
        //3
        long p = 80000;
        long q = 20000;
        long r = p * q;
        System.out.println("mul" +r);
    }
}
