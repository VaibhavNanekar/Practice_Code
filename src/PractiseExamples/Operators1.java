package PractiseExamples;

public class Operators1 {
    public static void main(String[] args) {
        //Postincrement and Preincrement
        int x =5;   //5
        int y =x++;  //6
        System.out.println(x);
        System.out.println(y);


        y=++x;
        System.out.println(y);
        System.out.println(y);
        System.out.println(y);
        y=--x;
        System.out.println(y);
    }
}


