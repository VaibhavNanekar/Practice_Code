package Operators;

public class RelationalOperator {
    public static void main(String[] args) {

        int a = 9;
        int b = 3;
        int c = 6;

        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a == c: " + (a == c));
        System.out.println("a != c: " + (a != c));

        long num1 = 300;
        long num2 = 100;
        long num3 = 400;

        System.out.println("num1 > num2: " + (num1 > num2));
    }
}
